package org.kr.scala.z80.test

import org.scalatest.funsuite.AnyFunSuite

class OpRotateShiftTest extends AnyFunSuite{

  test("run RLC r / RLCA") {
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("A",0x01)),List((0x0000,0x07)),"A",0x0000,0x02,"00_0_000")
    TestUtils.testRegOrAddrWithFlags(List(("F",0xFF),("A",0x01)),List((0x0000,0xCB),(0x0001,0x07)),"A",0x0000,0x02,"11_0_100",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("B",0x80)),List((0x0000,0xCB),(0x0001,0x00)),"B",0x0000,0x01,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("C",0x81)),List((0x0000,0xCB),(0x0001,0x01)),"C",0x0000,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("D",0x81)),List((0x0000,0xCB),(0x0001,0x02)),"D",0x0000,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("E",0x81)),List((0x0000,0xCB),(0x0001,0x03)),"E",0x0000,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("H",0x81)),List((0x0000,0xCB),(0x0001,0x04)),"H",0x0000,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("L",0x81)),List((0x0000,0xCB),(0x0001,0x05)),"L",0x0000,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("HL",0x0101)),List((0x0000,0xCB),(0x0001,0x06),(0x0101,0x81)),
      "",0x0101,0x03,"00_0_001",2)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("IX",0x0201)),List((0x0000,0xDD),(0x0001,0xCB),(0x0002,0x01),(0x0003,0x06),(0x0202,0x81)),
      "",0x0202,0x03,"00_0_001",4)
    TestUtils.testRegOrAddrWithFlags(List(("F",0x00),("IY",0x0201)),List((0x0000,0xFD),(0x0001,0xCB),(0x0002,0xFF),(0x0003,0x06),(0x0200,0x81)),
      "",0x0200,0x03,"00_0_001",4)
  }
}
